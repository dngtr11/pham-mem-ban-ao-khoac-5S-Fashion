    private static Random generator = new Random();

    public static int randomNumber(int min, int max) {
        return generator.nextInt((max - min) + 1) + min;
    }

    public String randomAlphaNumeric(int numberOfCharactor) {
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < numberOfCharactor; i++) {
            int number = randomNumber(0, ALPHA_NUMERIC.length() - 1);
            char ch = ALPHA_NUMERIC.charAt(number);
            sb.append(ch);
        }
        return sb.toString();
    }
    ChatLieu getForm() {
        return new ChatLieu(randomAlphaNumeric(8), tfTen.getText(), true);
    }

    private boolean checkForm() {
        if (tfTen.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tên");
            return false;
        }
        for (ChatLieu cl : service.getAll()) {
        if (tfTen.getText().equalsIgnoreCase(cl.getTenChatLieu())) {
            JOptionPane.showMessageDialog(this, "Đã có chất liệu này");
            return false;
        }
        }
        return true;
    }
    void showData(List<ChatLieu> list){
        model.setRowCount(0);
        for (ChatLieu cl : list) {
            model.addRow(new Object[]{
                cl.getMaChatLieu(),
                cl.getTenChatLieu()
            });
        }
    }
